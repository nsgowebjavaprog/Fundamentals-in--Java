
public class Arithmatic_Exp {
    public static void main(String[] args) {
        
        int a = 20, b = 10;

        int sum = a+b;
        float sub = a-b;
        int mul = a*b;
        float div = a/b;
        
        System.out.println("Sum: "+sum);
        System.out.println("Sub: "+sub);
        System.out.println("Mul: "+mul);
        System.out.println("Div: "+div);
    }
}
