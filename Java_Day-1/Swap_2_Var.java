
public class Swap_2_Var {
    public static void main(String[] args) {
        
        String x = "water";
        String y = "Kool-Aid";
            // swap
        String temp = x;
        x = y;
        y =temp;

        System.out.println("x : "+x);
        System.out.println("y : "+y);
    }
}
